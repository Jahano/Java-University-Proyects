    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String [] registros = new String [20];
        String[] titulos = {"ID","nombres","apellidos"};
        tabla = String.valueOf(jComboBox1.getSelectedItem());
        System.out.println(tabla);        
        campo = String.valueOf(jTextField1.getText());
        sql = "SELECT * FROM " +tabla +" WHERE id_cliente LIKE '%"+campo+"%'"
        +"OR nombres LIKE '%"+ campo + "%'"
        +"OR apellidos LIKE '%" + campo + "%';";
        
        try {
            Statement st;
            st = (Statement) connection.createStatement();            
            ResultSet rs = st.executeQuery(sql);
            model = new DefaultTableModel(null,titulos);
            
            while(rs.next())
            {
                registros[0]=rs.getString("id_cliente");
                registros[1]=rs.getString("nombres");
                registros[2]=rs.getString("apellidos");
                System.out.print(registros[0]+"\t"); 
                model.addRow(registros);
                System.out.print(registros[2] + "\t");
            }
            System.out.print(registros[1] + "\t");
            jTable1.setModel(model);
            
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

